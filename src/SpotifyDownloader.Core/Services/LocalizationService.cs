    public LocalizationService()
    {
        SetLanguage(AppLanguage.Portuguese);
    }

    public AppLanguage CurrentLanguage => _currentLanguage;

    public string GetString(string key)
    {
        return _strings.TryGetValue(key, out var value) ? value : key;
    }

    public string this[string key] => GetString(key);

    public string Format(string key, params object[] args)
    {
        var template = GetString(key);
        try { return string.Format(template, args); }
        catch { return template; }
    }

    public void SetLanguage(AppLanguage language)
    {
        _currentLanguage = language;

        if (AllStrings.TryGetValue(language, out var strings))
            _strings = strings;
        else
            _strings = AllStrings[AppLanguage.English];

        Log.Information("Language changed to {Language}", language);
        LanguageChanged?.Invoke(this, language);
    }

    public event EventHandler<AppLanguage>? LanguageChanged;
